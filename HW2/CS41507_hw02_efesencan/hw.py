def linear_congruence(a,b,n): # find the solutions of the linear congruences if any
    solutions = []
    print(gcd(a,n))
    if(b % gcd(a,n) == 0):
        d = gcd(a,n)
        x = (modinv(a//d,n//d)*(b//d)) % (n//d)
        solutions.append(x)
        count = 1
        for i in range(d-1):
            solutions.append(x + count*(n//d))
            count += 1
        return solutions
    else:
        return "NO SOLUTION"

